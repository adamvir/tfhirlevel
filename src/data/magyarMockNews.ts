export const getMagyarArticlesByCategory = (category: string): Article[] => {
  if (category === "home") return magyarMockArticles;
  return magyarMockArticles.filter(article => 
    article.category.toLowerCase() === category.toLowerCase()
  );
};

export const getMagyarArticleById = (id: string): Article | undefined => {
  return magyarMockArticles.find(article => article.id === id);
};